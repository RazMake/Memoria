# Recording script: Todo Editor visual task board
# =================================================
# Shows: The visual editor for .todo.md files with card interactions
#
# Prerequisites:
#   - Initialized Memoria workspace with Task Collector enabled
#   - A .todo.md file with 4-5 active tasks and 1-2 completed tasks
#   - Dark Modern theme, ~800×500px capture area
#
# Steps:
#   1. Open a .todo.md file — the visual Todo Editor appears
#   2. Show the task cards with checkboxes and text
#   3. Hover over a card to reveal the drag handle (⠿) and source link (🔗)
#   4. Click "+ Add task" in the toolbar — popup appears
#   5. Type a task name, press Enter to confirm — new card appears
#   6. Drag a task card to reorder (grab the drag handle, drop above another card)
#   7. Click a checkbox on an active task — it moves to the Completed section
#   8. Expand the Completed section to show completed tasks with date badges
#   9. Click a completed task's checkbox — it moves back to active
#  10. Double-click a task body to edit its text inline
