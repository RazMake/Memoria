# Recording script: Manage features
# ====================================
# Shows: Enabling/disabling features via the picker
#
# Prerequisites:
#   - Initialized Memoria workspace with some features enabled
#   - Dark Modern theme, ~800×500px capture area
#
# Steps:
#   1. Open the Command Palette (Ctrl+Shift+P)
#   2. Type "Memoria: Manage features" and select it
#   3. Multi-select picker appears with feature list (Explorer Decorations, Contacts, Task Collector)
#   4. Show the current state (some checked, some unchecked)
#   5. Toggle a feature off (e.g., uncheck "Explorer Decorations")
#   6. Toggle another feature on (e.g., check "Task Collector")
#   7. Click "OK" to confirm
#   8. Brief pause showing changes took effect (e.g., decorations disappear from Explorer)
